namespace Task_03_TemperatureConverter
{
    internal class Program
    {
        /*
         * Написать программу, которая выводит на экран таблицу соответствия температуры в градусах Цельсия и
         * Фаренгейта (F = C*1,8 + 32). Диапазон изменения температуры в градусах Цельсия и шаг должны вводиться во
         * время работы программы
         */
        static void Main(string[] args)
        {
            // Запрашиваем у пользователя начальную температуру в Цельсиях
            Console.WriteLine("Введите начальную температуру в градусах Цельсия:");
            double startCelsius = double.Parse(Console.ReadLine()); // Преобразуем ввод в число с плавающей точкой

            // Запрашиваем у пользователя конечную температуру в Цельсиях
            Console.WriteLine("Введите конечную температуру в градусах Цельсия:");
            double endCelsius = double.Parse(Console.ReadLine());   // Преобразуем ввод в число с плавающей точкой

            // Запрашиваем у пользователя шаг изменения температуры
            Console.WriteLine("Введите шаг изменения температуры в градусах Цельсия:");
            double step = double.Parse(Console.ReadLine());          // Преобразуем ввод в число с плавающей точкой

            // Выводим заголовок таблицы
            Console.WriteLine("\nТаблица соответствия температур:");
            Console.WriteLine("----------------------------------");
            Console.WriteLine("|  Цельсий  |  Фаренгейт  |");
            Console.WriteLine("----------------------------------");

            // Цикл для перебора температур в заданном диапазоне с заданным шагом
            for (double celsius = startCelsius; celsius <= endCelsius; celsius += step)
            {
                // Вычисляем температуру в Фаренгейтах по формуле
                double fahrenheit = celsius * 1.8 + 32;

                // Выводим строку таблицы с текущими значениями Цельсия и Фаренгейта
                // Используем форматирование для выравнивания колонок
                Console.WriteLine($"| {celsius,9:F1} | {fahrenheit,10:F1}   |");
            }

            Console.WriteLine("----------------------------------"); // Завершающая линия таблицы

            Console.ReadKey(); // Ожидание нажатия клавиши перед закрытием консоли
        }
    }
}