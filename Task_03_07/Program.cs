namespace Task_03_FreeFallVelocityTable
{
    internal class Program
    {
        /*
         * Написать программу, которая выводит таблицу скорости (через каждые 0,5с) свободно падающего тела v = g t ,
         * где g = 9,8 м/с2 –ускорение свободного падения.
         */
        static void Main(string[] args)
        {
            // Задаем ускорение свободного падения (g)
            const double g = 9.8; // м/с^2

            // Задаем начальное время (t)
            double time = 0; // секунды

            // Задаем шаг времени (dt)
            double timeStep = 0.5; // секунды

            // Задаем конечное время (ограничим таблицу до 5 секунд)
            double endTime = 5; // секунды

            // Выводим заголовок таблицы
            Console.WriteLine("----------------------");
            Console.WriteLine("|      Время (с) | Скорость (м/с) |");
            Console.WriteLine("----------------------");

            // Цикл для расчета и вывода значений скорости через заданный шаг времени
            while (time <= endTime)
            {
                // Вычисляем скорость свободно падающего тела v = g * t
                double velocity = g * time;

                // Выводим строку таблицы со значениями времени и скорости
                // Используем форматирование для выравнивания колонок
                Console.WriteLine($"| {time,12:F1}   | {velocity,14:F1}   |");

                // Увеличиваем время на шаг времени
                time += timeStep;
            }

            Console.WriteLine("----------------------"); // Завершающая линия таблицы
            Console.ReadKey(); // Ожидание нажатия клавиши перед закрытием консоли
        }
    }
}